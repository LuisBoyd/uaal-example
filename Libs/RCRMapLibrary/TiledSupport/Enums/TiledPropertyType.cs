namespace RCRMapLibrary.TiledSupport.Enums
{
    /// <summary>
    /// Represents property's value data type
    /// </summary>
    public enum TiledPropertyType
    {
        /// <summary>
        /// A String value
        /// </summary>
        String,
        /// <summary>
        /// A Bool value
        /// </summary>
        Bool,
        /// <summary>
        /// A Color value in Hex Format
        /// </summary>
        Color,
        /// <summary>
        /// A File path as a string
        /// </summary>
        File,
        /// <summary>
        /// A Float value
        /// </summary>
        Float,
        /// <summary>
        /// A Int value
        /// </summary>
        Int,
        /// <summary>
        /// An Object value which is the id of an object in the map
        /// </summary>
        Object
    }
}